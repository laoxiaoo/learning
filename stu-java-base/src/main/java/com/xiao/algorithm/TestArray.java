    public static int method1(int[] array) {
        //记录当前比对的对数的前一个数
        //如： 1 2  中的 1
        int last = 0;
        int lastCount = 0;
        for(int num : array) {
            if(lastCount == 0) {
                //记录两个数中的前一个数
                last = num;
                lastCount = 1;
            } else {
                if(last == num) {
                    lastCount++;
                } else {
                    //当不相等时，则-1，一般两个不相等的，此时需要减到0
                    lastCount--;
                }
            }
        }
        if(lastCount == 0) {
            return 0;
        } else {
            int length = array.length;
            //循环查出出现的次数
            int sum = 0;
            for(int num : array) {
                if(num == last){
                    sum ++;
                }
                if(sum>(length>>1)) {
                    return last;
                }
            }
        }
        return 0;
    }

